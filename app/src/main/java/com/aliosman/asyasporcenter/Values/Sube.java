package com.aliosman.asyasporcenter.Values;

public class Sube {
    String adi;
    String sifre;

    public Sube() {

    }

    public String getAdi() {
        return adi;
    }

    public void setAdi(String adi) {
        this.adi = adi;
    }

    public String getSifre() {
        return sifre;
    }

    public void setSifre(String sifre) {
        this.sifre = sifre;
    }

    public Sube(String adi, String sifre) {
        this.adi = adi;
        this.sifre = sifre;
    }
}
